package com.uwaterloo.smartpantry.stats;

public class RecipeTracking {
    private int recipeId;
    private int count;

    public int getRecipeId() {
        return recipeId;
    }

    public void setRecipeId(int recipeId) {
        this.recipeId = recipeId;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
